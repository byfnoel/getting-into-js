let integerValue = 42;
let floatValue = 3.14;

console.log(integerValue); // 42
console.log(floatValue); // 3.14

let stringValue = "Hello, friend.";

console.log(stringValue); // Hello, friend.

let posBoolValue = true;
let negBoolValue = false;

console.log(posBoolValue); // true
console.log(negBoolValue); // false

let nullValue = null;
let undefinedValue = undefined;

console.log(nullValue); // null
console.log(undefinedValue); // undefined

let arrayContainer = [1, 2, 3];

console.log(arrayContainer); // [1,2,3]

let objectValue = {
  name: "Francisco",
  age: "22",
};

console.log(objectValue); // { name: 'Francisco', age: '22' }
